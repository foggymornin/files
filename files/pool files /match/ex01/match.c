int		match(char *s1, char *s2)
{
	while(*s1 == *s2 || *s2 == '*')
	{
		if(!*s1)
		{
			while (*s2 == '*')
			   s2++;
			if (!*s2)
				return (1);
			else
				return (0);
		}
		while(*s2 == '*' && *s1)
			s1++;
		if (!*s1 && !*s2)
			return(1);
		if (*s1 == *s2)
		{
			s1++;
			s2++;
		}
	}
}
