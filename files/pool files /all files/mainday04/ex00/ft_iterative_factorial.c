int		ft_iterative_factorial(int nb)
{
	int i;
	int n;

	if (nb < 0 || nb > 13)
		return (0);
	i = 1;
	n = 1;
	if (nb == 0)
		return (1);
	while (i <= nb)
	{
		n *= i;
		i++;
	}
	return (n);
}
