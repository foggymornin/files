void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	int n;

	if (nb < 0)
		n = nb * -1;
	else
		n = nb;
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
		ft_putchar(n + '0');
}
