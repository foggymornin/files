void	ft_print_comb(void)
{
	char a;
	char b;
	char c;

	a = '0';
	b = a + 1;
	c = b + 1;
	while (a != '7' && b != '9')
	{
		ft_putchar(a);
		ft_putchar(b);
		ft_putchar(++c);
		if (a != '7')
		{
			ft_putchar(',');
			ft_putchar(' ');
		}
		if (c == '9')
			c = b++;
		if (b == 8)
		{
			a++;
			b = a + 1;
		}
	}
}
